using System;
using System.IO;

namespace NX_Suite.Core.Configuracion
{
    /// <summary>
    /// Single source of truth para todo lo que viaja con el .exe (no se baja
    /// del JSON): URLs por defecto del Gist, nombres de carpetas en
    /// <c>%AppData%</c>, archivos temporales del particionado, etiqueta de la
    /// SD, TTL del caché, etc.
    ///
    /// Cualquier constante "mágica" relacionada con paths, nombres de archivo
    /// o URLs hardcodeadas en el código debe vivir aquí.
    /// </summary>
    public static class ConfiguracionLocal
    {
        // ???????????????????????????????????????????????????????????????
        //  Identidad de la aplicación
        // ???????????????????????????????????????????????????????????????

        /// <summary>Nombre de la app — usado como carpeta raíz en <c>%AppData%</c>.</summary>
        public const string NombreApp = "NX-Suite";

        // ???????????????????????????????????????????????????????????????
        //  URLs remotas por defecto
        // ???????????????????????????????????????????????????????????????

        public const string UrlGistPrincipal =
            "https://gist.githubusercontent.com/ELCALLEJONGAMER/57949a130fdc307033492e365780a4bd/raw/NX-SWITE.json";

        public const string UrlGistBeta =
            "https://gist.githubusercontent.com/usuario/id_beta/raw/test.json";

        // ???????????????????????????????????????????????????????????????
        //  Manifiesto y nombres conocidos
        // ???????????????????????????????????????????????????????????????

        /// <summary>Nombre del manifiesto local que NX-Suite escribe en cada módulo instalado.</summary>
        public const string NombreManifiesto = ".nx-metadata.json";

        /// <summary>Nombre de la carpeta temporal que se crea en la raíz de la SD durante la instalación.</summary>
        public const string CarpetaTemporal = "NX_Temp";

        /// <summary>Etiqueta que se aplica a la partición principal de la SD tras formatear.</summary>
        public const string EtiquetaSwitchSd = "SWITCH SD";

        // ???????????????????????????????????????????????????????????????
        //  Caché del Gist
        // ???????????????????????????????????????????????????????????????

        /// <summary>
        /// TTL del caché local del JSON, en horas. Si el caché tiene menos de
        /// este tiempo se usa directamente sin descargar. <c>0</c> = forzar
        /// siempre la descarga.
        /// </summary>
        public const double TtlCacheGistHoras = 0;

        public const string NombreCacheGist = "gist_cache.json";

        // ???????????????????????????????????????????????????????????????
        //  Archivos temporales del particionado / formateo
        // ???????????????????????????????????????????????????????????????

        public const string NombreFat32FormatExe   = "fat32format.exe";
        public const string NombreFat32FormatZip   = "nxsuite_fat32format.zip";
        public const string NombreFat32FormatTemp  = "nxsuite_fat32format_tmp";
        public const string NombreDiskpartScript   = "nxsuite_diskpart.txt";

        // ???????????????????????????????????????????????????????????????
        //  Rutas calculadas en %AppData%\NX-Suite\…
        // ???????????????????????????????????????????????????????????????

        public static string RutaAppData         { get; } = Path.Combine(
            Environment.GetFolderPath(Environment.SpecialFolder.ApplicationData), NombreApp);

        public static string RutaCache           { get; } = Path.Combine(RutaAppData, "Cache");
        public static string RutaCacheZips       { get; } = Path.Combine(RutaCache,    "Zips");
        public static string RutaCacheExtraccion { get; } = Path.Combine(RutaCache,    "Extracted");
        public static string RutaCacheIconos     { get; } = Path.Combine(RutaCache,    "Icons");
        public static string RutaCacheSonidos    { get; } = Path.Combine(RutaCache,    "Sounds");
        public static string RutaCacheGist       { get; } = Path.Combine(RutaCache,    NombreCacheGist);
        public static string RutaBackups         { get; } = Path.Combine(RutaAppData,  "Backups");
    }
}
